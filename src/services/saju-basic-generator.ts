import type { SajuPillars } from './saju-calculator';

// 천간 인덱스 (갑~계 = 0~9)
const HEAVENLY_IDX: Record<string, number> = {
  갑: 0, 을: 1, 병: 2, 정: 3, 무: 4,
  기: 5, 경: 6, 신: 7, 임: 8, 계: 9,
};

// 지지 인덱스 (자~해 = 0~11)
const EARTHLY_IDX: Record<string, number> = {
  자: 0, 축: 1, 인: 2, 묘: 3, 진: 4, 사: 5,
  오: 6, 미: 7, 신: 8, 유: 9, 술: 10, 해: 11,
};

function seededRandom(seed: number) {
  let s = seed;
  return () => {
    s = (s * 16807 + 0) % 2147483647;
    return (s - 1) / 2147483646;
  };
}

function pillarsToSeed(p: SajuPillars): number {
  const fp = p.fourPillars;
  return (
    (HEAVENLY_IDX[fp.year.heavenly] ?? 0) * 100000 +
    (EARTHLY_IDX[fp.year.earthly] ?? 0) * 10000 +
    (HEAVENLY_IDX[fp.month.heavenly] ?? 0) * 1000 +
    (EARTHLY_IDX[fp.month.earthly] ?? 0) * 100 +
    (HEAVENLY_IDX[fp.day.heavenly] ?? 0) * 10 +
    (EARTHLY_IDX[fp.day.earthly] ?? 0)
  ) + 1;
}

